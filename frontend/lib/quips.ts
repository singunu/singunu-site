/** 셔플백 — 풀 전체가 소진될 때까지 같은 멘트가 반복되지 않는다 */
export function makeQuipBag(pool: string[]) {
  let bag: string[] = [];
  return () => {
    if (bag.length === 0) {
      bag = [...pool];
      for (let i = bag.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [bag[i], bag[j]] = [bag[j], bag[i]];
      }
    }
    return bag.pop() as string;
  };
}
